    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // TODO add your handling code here:
        int linha = tblPedidos.getSelectedRow();
        if (linha >= 0) {
            int id = Integer.parseInt(tblPedidos.getValueAt(linha, 0).toString());
            boolean confirmado = new PedidoDAO().confirmarPedido(id);

            if (confirmado) {
                JOptionPane.showMessageDialog(this, "Pedido confirmado com sucesso!");
                carregarPedidosPendentes();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao confirmar pedido.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um pedido.");
    }
    }//GEN-LAST:event_btnConfirmarActionPerformed

